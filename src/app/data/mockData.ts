// Helper function to calculate stats from call records
export function calculateCallStats(calls: CallRecord[]): CallStats {
  return {
    totalCalls: calls.length,
    rnr: calls.filter(c => c.status === 'RNR').length,
    notReachable: calls.filter(c => c.status === 'Not Reachable').length,
    wrongNumber: calls.filter(c => c.status === 'Wrong Number').length,
    answeredByFamily: calls.filter(c => c.status === 'Answered by Family').length,
    answeredByCustomer: calls.filter(c => c.status === 'Answered by Customer').length,
    answeredByOthers: calls.filter(c => c.status === 'Answered by Others').length,
    // Response tags
    promisedToPay: calls.filter(c => c.responseTag === 'Promised to Pay').length,
    requestedTime: calls.filter(c => c.responseTag === 'Requested Time').length,
    deniedToPay: calls.filter(c => c.responseTag === 'Denied to Pay').length,
    askedToCallBack: calls.filter(c => c.responseTag === 'Asked to call back').length,
    othersTag: calls.filter(c => c.responseTag === 'Others').length,
  };
}